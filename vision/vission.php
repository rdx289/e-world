<h3> Vision</h3>
<hr>
<p>
To be a premier academic institution, recognized  for its contribution to industry and society through excellence in teaching, learning,  rebuilding hidden talent and leadership. 
To evolve skilled and value based resource professional , to provide opportunities to the students  without any discrimination , to find space for the exponential growth of personalities  and character.
Education and empowerment to face the challenges of tomorrow successfully through quality education.<br><br>
e-World Institute of Computer Studies is an institution of learning that belives in the Power of Almighty God who has endowed every human  being the special 
gift of knowledge and natural capabilities that can make one self-reliant, and that every individual, no matter how physically, mentally and economically
 disadvantaged can still be shaped into a responsible and productive person who can become a catalyst of change for the advancement and progress of his country.
 

<br><br>Thus, e-World envisions its graduate to be a total person who is:
<ul>
<li>Physically fit, emotionally stable, morally sound and intellectually prepared.</li>
<li>responsible individual with high integrity.</li>
<li>service-oriented persons who is responive to the  moral, spiritual, cultural, social and economic needs of his country.</li>
<li>innovative and active participants in the shaping of the future IT generations.</li></ul>
</p>	
</div>
<div class="mission">
 <h3> Mission</h3>
 <hr>
<p>Technology plays a vital role in meeting the expectations of an advanced world that is fast changing and rapidly increasing in complexity and demands.
 The Philippines,as a member of international community,must address its own priorities by producing human resources that can cope with today's world of 
 information technology. In response to the global demand of IT manpower, e-world takes upon itself the mission of producing graduates that possess the 
 necessary skills and right attitude that could qualify them to join the long race of the best IT people that  this world can produce. To this, e-world is 
 committed to:

Provide its students an affordable but high quality education: balance their technological education with value-oriented studies: give students amaximum
 level of support for their development by providing them responsible and professionally competent faculty, adequate facilities, seminars that keep them 
 abreast of the latest technological breakthrough, and sufficient practical training through simulated office experience as well as linkage with reputable 
 business establishments.
		</p> 

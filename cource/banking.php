	<h2>Banking </h2><hr>
	Banking is one of the fastest growing industry in India and they are considered as sunrise sector of Indian Economy. More so, the government is focusing on providing banking facilities in every village and town,
	under its Financial Inclusion scheme. And hence, more and more branches of the banks are being opened in every part of the country.
	<br><br>
	As the sector is growing very fast, it also needs manpower at various levels. 
	Essentially the recruitment in bank sector happens at the following levels:
	<br>
	<br>
	<b>Bank Probationary Officer (Bank PO):</b> This is a managerial position in a bank. After the essential 1 to 2-year training, candidates are designated as Assistant Managers (AM) or Deputy Managers (DM).
	Starting Salary as a Bank PO ranges from Rs. 30,000 - Rs. 40,000, depending upon bank to bank and location of posting.
	<br>
	<br>
	<b>Bank Clerks:</b> As the name suggests, this is a clerical position in the bank and this can be considered as entry level job in a bank.
	Salary of a Bank Clerk is generally in the range of Rs.
	20,000-25,000 depending on bank to bank and posting location.
	<br><br>
	<b>Specialist Officers (SO):</b> Banks also hire candidates for specialist profiles like – IT Officer, HR Officer, Marketing Officer, Finance Officer, Law Officer, Agriculture Officer etc. Usually, 
	the grade and the starting salary of Specialist Officer is in the same range as Probationary Officers.
	<br><br>
	Every year, government banks recruit more than 1,00,000 candidates for various roles. So in the terms of the employment in government sector, 
	Banking is the most lucrative sector to grab a government job.

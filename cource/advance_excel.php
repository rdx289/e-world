	<b>Advanced Excel</b> is a software program included in the Microsoft Office suite. It is used to create spreadsheets, which are documents in which data is laid out in rows and columns — like a big table. 
		  Due to its extreme versatility and power, Excel has become one of the most-used software programs in the business world since its launch in 1985. Indeed, the personal computing renaissance of the 1980s and 1990s was largely driven by the many uses of Excel and other spreadsheet software.
		 <br>
		 <b>Why do people use Excel</b><br>
		  There are many reasons people may use Excel (a spreadsheet program). For example, someone might use Excel to keep track of their expenses. For a full list of reason and examples of how people use a spreadsheet, see our spreadsheet definition.
		 
     <br>
	 That's all well and good, but what are spreadsheets used for? Almost everything. Spreadsheets are extraordinarily powerful tools, and are used frequently in the business world to store and manipulate data.

Throughout the course of these tutorials, we'll be following SnackWorld, a fictional company, as it uses Excel to track data about its business. Here's one example of how SnackWorld uses Excel in the workplace

As you can see, SnackWorld has used the above Excel spreadsheet to keep track of its sales numbers by item sold. The tabular format of a spreadsheet is the perfect way to measure this complex set of data — it would take a long time for SnackWorld's managers to write this out by hand! And Excel's formulas and functions, which we'll learn later on in this module, allow employees to easily pull out specific pieces of data to answer managers' questions.</p>


	 
